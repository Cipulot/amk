void custom_board_init(void)
{
    SystemClock_Config();

    MX_GPIO_Init();
    MX_DMA_Init();

#if defined(TINYUSB_ENABLE)
    MX_USB_DEVICE_Init();
#endif

#ifdef USE_I2C1
    MX_I2C1_Init();
#endif

#ifdef USE_I2C2
    MX_I2C2_Init();
#endif

#ifdef USE_SPI1
    MX_SPI1_Init();
#endif

#ifdef USE_SPI2
    MX_SPI2_Init();
#endif

#ifdef USE_SPI3
    MX_SPI3_Init();
#endif

    MX_RTC_Init();

#ifdef USE_PWM_TIM2
    MX_TIM2_Init();
#endif

#ifdef USE_UART2
    MX_USART2_UART_Init();
#endif

#ifdef DYNAMIC_CONFIGURATION
#ifdef FORCE_MSC_MODE
    usb_setting |= USB_MSC_BIT;
#else
    uint32_t magic = HAL_RTCEx_BKUPRead(&hrtc, RTC_BKP_DR1);
    //if (magic == 0) {
    if (magic > 0 ) {
        usb_setting |= USB_MSC_BIT;
    } else {
        usb_setting = 0;
    }
#endif
    HAL_PWR_EnableBkUpAccess();
    HAL_RTCEx_BKUPWrite(&hrtc, RTC_BKP_DR1, 0);
#endif

#ifdef WDT_ENABLE
    MX_IWDG_Init();
#endif
}

